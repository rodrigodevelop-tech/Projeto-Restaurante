    private void chkRetiraTaxaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkRetiraTaxaActionPerformed
        if (cmbFormaPagamento.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma Forma de\nPagamento!",
                    "Fechamento de Venda", JOptionPane.INFORMATION_MESSAGE);
            chkRetiraTaxa.setSelected(false);
        } else {
            if (chkRetiraTaxa.isSelected()) {
                txtTaxaServico.setText("0.00");
                calculaVPagar();
            } else {
                txtTaxaServico.setText(decimal.format(Float.valueOf(txtSubTotal.getText()) * 0.10).replaceAll(",", "."));
                calculaVPagar();
            }
        }
    }//GEN-LAST:event_chkRetiraTaxaActionPerformed

