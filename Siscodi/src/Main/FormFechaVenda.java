    //seta os valores iniciais dos textboxs
    public void valores(String numMesa,String valorD,DefaultTableModel itens){
        valorD = valorD.replaceAll(",",".");
        lbNumMesa.setText(numMesa);
        txtSubTotal.setText(valorD);
        txtTaxaServico.setText(decimal.format(Float.valueOf(valorD)*0.10).replaceAll(",", "."));
        txtValorPagar.setText(String.valueOf(Float.valueOf(valorD)+Float.valueOf(txtTaxaServico.getText())));
        itensMesa = itens;
        if(cmbFormaPagamento.getSelectedItem().equals("A PRAZO")){
                btPesquisaCliente.setEnabled(true);
                jTNomeCliente.setVisible(true);
        }
    }

    //calcula o valor da compra automaticamente.
    public void calculaVPagar() {
        //Converto os valores e salvo em variaveis para não me perder no codigo
        float troco = (Float.valueOf(txtValorRecebido.getText()) - Float.valueOf(txtValorPagar.getText()));
        float taxa = Float.valueOf(txtTaxaServico.getText());
        //Pega o valor do desconto.
        float desc;
        if (txtDesconto.getText().equals("") || txtDesconto.getText().equals(",")) {
            desc = 0;
        } else {
            desc = Float.valueOf(txtDesconto.getText());
        }
        
        float Valorp = (Float.valueOf(txtSubTotal.getText())+taxa)-desc;
        float ValorR = Float.valueOf(txtValorRecebido.getText());
        
        //verifica forma de pagamento  e aplica a melhor opção para o mesmo.
        if (cmbFormaPagamento.getSelectedIndex() != -1) {
            if (cmbFormaPagamento.getSelectedItem().equals("DINHEIRO")) {
                
                txtValorPagar.setText(String.valueOf(Valorp));
                if (troco >= 0) {
                    txtTroco.setText(decimal.format(troco).replaceAll(",","."));
                } else {
                    txtTroco.setText("0.00");
                }
            } else if (Valorp >= ValorR) {
                txtValorPagar.setText(decimal.format(Valorp).replaceAll(",","."));
                txtTroco.setText("0.00");
  
            }else {
                JOptionPane.showMessageDialog(this, "Impossivel Debitar valor maior\nque o valor devido!",
                        "Fechamento da Venda", JOptionPane.INFORMATION_MESSAGE);
                txtValorRecebido.setText("0.00");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma Forma de\nPagamento!",
                    "Fechamento de Venda", JOptionPane.INFORMATION_MESSAGE);
            
        }

    }
    
    //retorna a data formatada para salvar no banco de dados
    public String retornaData(){
        data = new Date();
        String aux = DATA.format(data);
        return aux;
    }
    
    //preenche os dados da compra na classe para salvar no banco de dados
    public void preencheClasse(){
        if (vendaMesa == null){
            vendaMesa = new ClasseVendaMesa();
        }
        vendaMesa.setIdMesa(Integer.valueOf(lbNumMesa.getText()));
        vendaMesa.setTotalVenda(Float.valueOf(txtValorPagar.getText()));
        vendaMesa.setItensMesa(itensMesa);
        vendaMesa.setDataVenda(retornaData());
        vendaMesa.setFormaPagamento((String) cmbFormaPagamento.getModel().getSelectedItem());
    }
    
    
    
