    private void btFechaVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btFechaVendaActionPerformed
        if (JOptionPane.showConfirmDialog(this, "Deseja Finalizar a venda?", "PubManager.Soft",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            if (Float.valueOf(txtValorRecebido.getText()) >= Float.valueOf(txtValorPagar.getText())) {
                preencheClasse();
                if (vendaMesa.cadastraVenda(vendaMesa)) {
                    if(!jTNomeCliente.getText().equals("Cliente nao localizado!")){
                       vendaMesa.cadastrarVendaAprazo(vendaMesa, tbClientes);
                    }else{
                        JOptionPane.showMessageDialog(this, "Informe o cliente",
                            "PubManager.Soft", JOptionPane.INFORMATION_MESSAGE);
                    }
                    JOptionPane.showMessageDialog(this, "Venda Efetuada com Sucesso",
                            "PubManager.Soft", JOptionPane.INFORMATION_MESSAGE);
                    telaPai.setModal(false);
                    this.setModal(false);
                    telaPai.getObjMesa().finalizaMesa();
                    this.setVisible(false);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Não Foi Possivel Realizar a venda!\nErro no Banco de Dados",
                            "PubManager.Soft", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Valor Recebido Menor que o Valor Devido!",
                        "PubManager.Soft", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btFechaVendaActionPerformed

